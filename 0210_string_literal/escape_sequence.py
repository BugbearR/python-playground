# https://docs.python.org/ja/3/reference/lexical_analysis.html#escape-sequences

# continue line by backslash
print("abc\
def")

# backslash
print("\\")

# single quote
print("\'")

# double quote
print("\"")

# bell
print("\a")

# backspace
print("abc\bdef")

# form feed
print("abc\fdef")

# line feed
print("abc\ndef")

# carriage return
print("abc\rdef")

# horizontal tab
print("abc\tdef")

# vertical tab
print("abc\vdef")

# octal value
print("\101")

# hex value
print("\x41")
